    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Models\Post  $post
     * @return \Illuminate\Http\Response
     */
    public function destroy(PostDestroyRequest $request, Post $post)
    {
       $post->delete();
       return redirect()->route('blog');
    }
